public class switchcase{
    
        public static void main(String args[])
        {
            int num=3;
            switch(num) {
                case 1:System.out.println("sike");
                break;
                case 2:System.out.println("nigga");
                break;
                case 3:System.out.println("shake");
                default:System.out.println("smd");
            }
    }
}